public class calculadora {
    
    // Adição
    public static int adicionar(int a, int b) {
        return a + b;
    }

    // Subtração
    public static int subtrair(int a, int b) {
        return a - b;
    }

    // Multiplicação
    public static int multiplicar(int a, int b) {
        return a * b;
    }
}
